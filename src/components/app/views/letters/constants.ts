export function getAlphabet(language: string): LetterInfo[] {
  // Normalize match
  const foundKey = Object.keys(SUPPORTED_ALPHABETS).find(
    (key) => key.toLowerCase() === language.toLowerCase()
  );
  
  if (foundKey) {
    return SUPPORTED_ALPHABETS[foundKey].map((char) => ({ char }));
  }

  // Fallback to English if explicitly unknown
  return SUPPORTED_ALPHABETS["English"].map((char) => ({ char }));
}

/** Randomize array in-place using Fisher-Yates */
export function shuffleArray<T>(array: T[]): T[] {
  const newArr = [...array];
  for (let i = newArr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArr[i], newArr[j]] = [newArr[j], newArr[i]];
  }
  return newArr;
}
